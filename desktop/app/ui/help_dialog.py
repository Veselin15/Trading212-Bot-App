"""Scrollable help guide for the desktop app."""
from __future__ import annotations

from PySide6.QtCore import Qt
from PySide6.QtWidgets import (
    QDialog,
    QDialogButtonBox,
    QFrame,
    QHBoxLayout,
    QLabel,
    QPushButton,
    QScrollArea,
    QVBoxLayout,
    QWidget,
)

from .theme import _BORDER, _MUTED, _SURFACE2, _SKY, _SKY_DIM, _TEXT, _WARN, _SUCCESS, _DANGER


def _section(icon: str, title: str, body: str) -> QWidget:
    w = QWidget()
    lay = QVBoxLayout(w)
    lay.setContentsMargins(0, 0, 0, 0)
    lay.setSpacing(8)

    title_row = QHBoxLayout()
    title_row.setSpacing(10)
    title_row.setContentsMargins(0, 0, 0, 0)

    icon_lbl = QLabel(icon)
    icon_lbl.setStyleSheet(f"font-size: 16pt; background: transparent; color: {_SKY};")
    icon_lbl.setFixedWidth(28)
    icon_lbl.setAlignment(Qt.AlignmentFlag.AlignTop | Qt.AlignmentFlag.AlignHCenter)
    title_row.addWidget(icon_lbl)

    t = QLabel(title)
    t.setObjectName("HelpSectionTitle")
    t.setWordWrap(True)
    t.setAlignment(Qt.AlignmentFlag.AlignVCenter)
    title_row.addWidget(t, 1)

    lay.addLayout(title_row)

    b = QLabel(body)
    b.setObjectName("HelpSectionBody")
    b.setWordWrap(True)
    b.setTextInteractionFlags(Qt.TextInteractionFlag.TextSelectableByMouse)
    b.setContentsMargins(38, 0, 0, 0)
    lay.addWidget(b)

    return w


def _sep() -> QWidget:
    line = QWidget()
    line.setFixedHeight(1)
    line.setStyleSheet(f"background: {_BORDER};")
    return line


def _callout(text: str, color: str = _SKY_DIM, border: str = _SKY) -> QFrame:
    f = QFrame()
    f.setStyleSheet(
        f"QFrame {{ background-color: {color}; border: 1px solid {border}; border-radius: 8px; }}"
    )
    lay = QVBoxLayout(f)
    lay.setContentsMargins(14, 10, 14, 10)
    lbl = QLabel(text)
    lbl.setObjectName("HelpSectionBody")
    lbl.setWordWrap(True)
    lbl.setStyleSheet(f"color: {_TEXT}; border: none; background: transparent;")
    lay.addWidget(lbl)
    return f


