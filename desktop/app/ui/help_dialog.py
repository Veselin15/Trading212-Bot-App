    footer.addWidget(goto_setup)
    footer.addStretch(1)

    btns = QDialogButtonBox(QDialogButtonBox.StandardButton.Close)
    btns.button(QDialogButtonBox.StandardButton.Close).clicked.connect(dlg.accept)  # type: ignore[arg-type]
